def format_example(example):
    messages = [
        {
            "role": "system",
            "content": "You are a clinical information extraction assistant. Output ONLY valid JSON."
        },
        {
            "role": "user",
            "content": example["input"]
        },
        {
            "role": "assistant",
            "content": json.dumps(example["output"], ensure_ascii=False)
        }
    ]

    text = tokenizer.apply_chat_template(
        messages,
        tokenize=False,
        add_generation_prompt=False
    )
    return {"text": text}

train_ds = dataset["train"].map(format_example)
val_ds = dataset["validation"].map(format_example)

