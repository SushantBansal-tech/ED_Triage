def predict(text):
    messages = [
        {
            "role": "system",
            "content": "You are an emergency triage assistant. Output ONLY valid JSON with esi_level and reason."
        },
        {
            "role": "user",
            "content": text
        }
    ]
    prompt = tokenizer.apply_chat_template(
        messages,
        tokenize=False,
        add_generation_prompt=True
    )
    inputs = tokenizer(prompt, return_tensors="pt").to(model.device)

    with torch.no_grad():
        out = model.generate(
            **inputs,
            max_new_tokens=120,
            do_sample=False
        )

    return tokenizer.decode(out[0], skip_special_tokens=True)

